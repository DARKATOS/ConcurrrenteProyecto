DROP PROCEDURE IF EXISTS ACTUALIZAR_CUADRO;
DELIMITER $$
CREATE PROCEDURE ACTUALIZAR_CUADRO(COLOR_P VARCHAR(100),NUMERO_P INTEGER, IDCARA_P INTEGER)
BEGIN
	UPDATE CUBO SET COLOR=COLOR_P WHERE NUMERO=NUMERO_P AND IDCARA=IDCARA_P;
END$$
DELIMITER ;

